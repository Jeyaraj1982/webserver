						<p class="p-space"> &nbsp;&nbsp;&nbsp;&nbsp; <i class="fa fa-check-square"></i>&nbsp;&nbsp;&nbsp;Availability of various online recharge websites for easy mobile 
						top up using credit card, debit card, net banking and ATM cards.</p>
						<p class="p-space"> &nbsp;&nbsp;&nbsp;&nbsp; <i class="fa fa-check-square"></i>&nbsp;&nbsp;&nbsp;Prepaid mobile connections are best suited for those with medium to low usage habits, 
						and do not want to enter into monthly contracts.</p>
						<p class="p-space"> Currently, a lot of mid-market mobile subscribers prefer value added services such as broadband 3G internet, 
						online media like images and video, caller tunes, etc.</p>
						<p class="p-space">Prepaid mobile recharge SIM connections can be bought over the counter at most mobile vendors and dealers. In contrast, post paid connections require a lot of formalities
						like personal ID proof,  address proof, document verification, minimum monthly payments etc.</p>
						<p class="p-space"> There are also age limits when it comes to getting a post-paid connection, which puts young users like students 
						at a disadvantage.</p>
						<p class="p-space"> Postpaid connections are also popular due to increased control over mobile usage, and subscribers have the option of easily switching over to a competitor’s network if 
						the pricing model is not favourable.</p>

						
