 <h2 class="heading-cascade heading-cascade--danger" style="border-bottom:1px #cbcbcb solid; padding-bottom:1px" >Recharging your wireless internet data card</h2>
						<p class="p-space">India boasts of over 150 million users accessing internet using mobile phones, smart phones, 
						PDAs, Tablets and laptops every day. In the past decade there has been an exponential growth in
						broadband internet connections with over 16.08 million subscribers.</p>


						<p class="p-space">A number of private internet providers have entered the market with their private broadband infrastructure, 
						and have a range of options when it comes to both wired and wireless internet data access.</p>
						
 <p class="p-space">Options available now include wireless internet data cards, dongles, e-sticks, USB data card sticks,
 2G/3G/4G/LTE mobile internet and high speed broadband internet plan.</p>
						
						<p class="p-space">Online data cards – USB sticks, dongles, e-sticks etc.</p>
						
						<p class="p-space">Wireless data card or e-sticks provide subscribers the ability to get online anywhere in India, 24X7,
						without the need for a fixed internet connection.</p>
						
						<p class="p-space">This is especially helpful for working professionals, travelling salesmen, and other users who need internet access on the go. There are various internet data card providers in
						India with different quality of service, speed, cost and connectivity.</p>
						
						<p class="p-space">3G mobile internet</p>
						
						<p class="p-space">3G mobile internet gives subscribers the ability to make video calls, play online games, check emails,
						surf internet, access online TV, update social network profiles and download files.</p>
						
						<p class="p-space">A smartphone with a prepaid or postpaid mobile connection with internet access as an add-on
						/value added is another option to access internet on the go.</p>
						
						<p class="p-space">Leading mobile phone service providers offer 2G, 3G, LTE internet services through data cards. The main advantage of these data cards is portability,
						mobility and ability to recharge the data card online.</p>
						
						<p class="p-space">Leading players in the online data card recharge market include Tata Photon plus, MTS MBlaze  and Reliance NetConnect. 
						3G mobile internet providers include Airtel, BSNL, Idea, and Aircel.</p>
						
