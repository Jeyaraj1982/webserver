						<p class="p-space">An interesting fact is that India has around 916 million cell phone connections, but only 312
						million subscribers. This basically means that on average, each user has around 3 SIM cards.</p>
						<p class="p-space">This is because most subscribers in India tend to switch prepaid SIM cards connections based on
						the newest and most economical prepaid plans.</p>
						<p class="p-space">There are various reasons for such a high usage of prepaid mobile services. Some of them are</p>
