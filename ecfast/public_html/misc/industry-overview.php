<p class="p-space">There is an increasing trend towards using rich applications and video services on the go. This means that users are demanding more than the usual speeds that come with a typical 2G
 connection.</p>
		<p class="p-space">Even though 3G providers are working towards laying down a strong strategy to provide broadband mobile, the cost of delivering it is quite high. Right now is is the CDMA players like Tata Photon plus, Reliance Netconnect and MTS MBlaze who are ruling the roost when it comes to high speed mobile Internet
		access through data cards.</p>
<p class="p-space">When it comes to online  recharge, pricing is probably the most crucial. Mobile internet providers are experimenting with innovative pricing models.</p>
<p class="p-space">Online data card recharge tariffs have various pricing models such Pay as you go, Standard, Sachet, FlexiShield, Volume-based recharges and so no.</p>
<p class="p-space">There is also cost-effective bundling with handsets and tariff plans with value added features like mobile TV, mobile gaming etc.  
Subscribers tend to prefer the e-sticks and dongles due to its ease of use.</p> 
<p class="p-space">Improvement in 3G mobile internet services has been taking place for a while, 
but for now users are still going for internet data cards as their first choice when it comes to mobile internet.
</p> 

  <h2 class="heading-cascade heading-cascade--danger" style="border-bottom:1px #cbcbcb solid; padding-bottom:1px" >DTH RECHARGE</h2>
						<p class="p-space">Here are 3 basic televisions medium in India: Analog (cable TV), digital Set Top Boxes (STB),
						and digital Direct to Home (DTH). Analog cable TV has been around for over 25 years but is essentially limited in the range of channels, features and value added services.  Digital services like STB and DTH come 
						with superior audio and video quality and have attracted a large number of subscriber base in India.
 </p>
 
 			<p class="p-space">The Indian DTH industry revenue for 2012 was close to $ 1.6 billion with around 33 million subscribers
			(the user based is forecasted to grow to 82 million by 2020). The forecast for the Direct-To-Home (DTH) market share in India
			is close to $ 5.3 billion by 2020. India is expected to become the largest direct to home (DTH) market in the world. Government 
			of India’s DAS (digitization of cable signals) drive has given a boost to online DTH recharge subscription industry.
 </p>
 
 
 			<p class="p-space">Zee’s Dish TV is the market leader with 27% market share, and Videocon d2h comes a close second.
			Tata Sky has 18% share and Airtel Digital TV has 21% market share.
 </p>
 
 
 			<p class="p-space">DAS (digitization of cable signals) is being rolled out in four waves.</p>
			
 			<p class="p-space">&nbsp;&nbsp;&nbsp;&nbsp; <i class="fa fa-check-square"></i>&nbsp;&nbsp;&nbsp; Wave 1: the four leading metros in India (Delhi, Chennai, Mumbai and Kolkota) transitioned to set top boxes (STBs) by December 31, 2012</p>
 			<p class="p-space">&nbsp;&nbsp;&nbsp;&nbsp; <i class="fa fa-check-square"></i>&nbsp;&nbsp;&nbsp; Wave 2: 38 India cities across 15 states switched to set top boxes (STBs) by March 31, 2013.</p>
 			<p class="p-space">&nbsp;&nbsp;&nbsp;&nbsp; <i class="fa fa-check-square"></i>&nbsp;&nbsp;&nbsp; Wave 3: All India urban areas have been mandated to transition to STBs by September 30, 2014</p>
 			<p class="p-space">&nbsp;&nbsp;&nbsp;&nbsp; <i class="fa fa-check-square"></i>&nbsp;&nbsp;&nbsp; Wave 4: all of India has to move to STBs by December 31, 2014</p>
 			<p class="p-space">This has ensured fierce competition in the digital cable services and digital DTH services. The Indian DTH industry today has 6 service providers –</p>
  			<p class="p-space">&nbsp;&nbsp;&nbsp;&nbsp; <i class="fa fa-check-square"></i>&nbsp;&nbsp;&nbsp; Dish TV</p>
  			<p class="p-space">&nbsp;&nbsp;&nbsp;&nbsp; <i class="fa fa-check-square"></i>&nbsp;&nbsp;&nbsp; Sun Direct</p>
  			<p class="p-space">&nbsp;&nbsp;&nbsp;&nbsp; <i class="fa fa-check-square"></i>&nbsp;&nbsp;&nbsp; Videocon D2H</p>
  			<p class="p-space">&nbsp;&nbsp;&nbsp;&nbsp; <i class="fa fa-check-square"></i>&nbsp;&nbsp;&nbsp; Tata Sky</p>
  			<p class="p-space">&nbsp;&nbsp;&nbsp;&nbsp; <i class="fa fa-check-square"></i>&nbsp;&nbsp;&nbsp; Airtel Digital TV</p>
  			<p class="p-space">&nbsp;&nbsp;&nbsp;&nbsp; <i class="fa fa-check-square"></i>&nbsp;&nbsp;&nbsp; Reliance Digital TV</p>
  			<p class="p-space">The DTH market in India has grown quite rapidly in the past few years. In 2012, 22% of Indian households have at least one STB (expected to grow to 39% by 2015 and close 
			to 50% by 2020). DTH penetration is projected to reach 90% in the long-term. The Telecom Regulatory Authority of India (TRAI) has given 
			recommendations to increase the FDI limit in DTH to 74 percent from the 49 percent. This will promote consolidation in the DTH market.
			TRAI has also recommended that FDI in IPTV and mobile TV be capped at 74 percent.</p>
			
			<p class="p-space">Also, the rural market is turning out to be a larger revenue contributor than the urban counterparts.
			At present, out of 26 million DTH users, 62% are from rural areas and smaller towns in India. Increased disposable income along with innovative marketing by the DTH providers is responsible for this. 
			Availability of low cost regional language channels is contributing to rural DTH growth.</p>
			
			<p class="p-space">To add to the DTH drama, public broadcaster DD Direct Plus is threatening the stronghold of the
			six private DTH providers (Tata Sky , Airtel Digital TV, Reliance Big TV, Videocon D2H, Zee Dish TV, and Sun Direct)
			which have more than 22 million users at present. Government of India is investing close to 82 crore rupees in
			DD Direct Plus with the aim of doubling its current subscriber base to over 14 million in the next 2 years.</p>
			
			
 
 
 
