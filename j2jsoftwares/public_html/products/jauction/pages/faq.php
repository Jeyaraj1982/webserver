    <div style="font-size:12px;line-height: 18px;"><b style="color:blue">Ans:</b>  Yes. Click on "Active Bids" under the My Account box, and you will see a list of your active bids (if any). To update the bid status in real time, click the "Refresh" button at the bottom of the page.</div>
