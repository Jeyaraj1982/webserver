    <div style="font-size:12px;line-height: 18px;"><b style="color:blue">Ans:</b> Wincashdeal will send you an e-mail or text informing you that you have won the auction. Once confirmed and verified as the winner, your name may also appear on the results pages as the winner of the auction.</div>
