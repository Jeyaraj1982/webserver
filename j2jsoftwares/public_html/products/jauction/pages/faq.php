    <div style="font-size:12px;line-height: 18px;"><b style="color:blue">Ans:</b> If you are facing any problem on IE to access website then follow below steps: 1. Go internet options menu > Security > Security level for this zone. Make it medium level 2. Click on custom level enable all required setting like JavaScript, ActiveX Control etc.</div>
