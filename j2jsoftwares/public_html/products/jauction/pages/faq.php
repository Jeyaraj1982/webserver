    <b style="color:green">Q: How do I know if my bid was successfully received?</b><br> 
    <div style="font-size:12px;line-height: 18px;"><b style="color:blue">Ans:</b>  Wincashdeal Reverse Auction will send you an e-mail and users also receive an instant notification on-screen once the bids are submitted, including details of the bid, unique Bd ID and real-time status.</div>
    <br>

