    <b style="color:green">Q: How can I place a bid in an auction?</b><br>   
    <div style="font-size:12px;line-height: 18px;"><b style="color:blue">Ans:</b> Easy. Simply login, then check your amount balance in My Account box. If you do not have sufficient balance, click on Buy Bids to deposit amount. If you have sufficient balance click on the auction you would like to enter, and click on Bid Now to place your bid(s).</div>
    <br>

    <b style="color:green">Q: Can I place more than one bid?</b><br>  
    <div style="font-size:12px;line-height: 18px;"><b style="color:blue">Ans:</b> You can place as many bids as you like, unless there is a stated maximum number of bids for each auction. However, please remember that an entry fee may be charged for each bid that is placed.</div>
    <br>

    <b style="color:green">Q: How do I pay for a bid?</b><br>
    <div style="font-size:12px;line-height: 18px;"><b style="color:blue">Ans:</b> Click on Buy Bids or follow the instructions in the bid process and it will take you to a secure payment page. You can pay debit / credit card /Bank account.</div>
